export const lang_mar = "marathi";
export const lang_eng = "english";
export const lang_kor = "korean";


export const setMarathiLang = (language) => ({
    type: lang_mar,
    payload: language,
})

export const setEnglishLang = (language) => ({
    type: lang_eng,
    payload: language,
})

export const setHindiLang = (language) => ({
    type: lang_kor,
    payload: language,
})